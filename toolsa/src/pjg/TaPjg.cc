#include <toolsa/TaPjg.hh>
#include <toolsa/pjg.h>
#include <toolsa/toolsa_macros.h>

double TaPjg::_earthRadiusKm = EARTH_RADIUS;   

/////////////////////////////////////////////////////////////////              
// Set earth radius to non-standard value                                      
                                                                                 
void TaPjg::setEarthRadius(double val) {                                       
  _earthRadiusKm = val;                                                         
  PJG_set_earth_radius(val);                                                   
}                                                                              
                                                                                 
